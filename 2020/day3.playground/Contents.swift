var grid = input.split(separator: "\n")

func traverseGrid(grid: [String.SubSequence], right: Int, down: Int) -> Int {
    var trees = 0
    var position = 0
    var count = 0
    
    while count < grid.count {
        let row = grid[count]
        if "#" == row[row.index(row.startIndex, offsetBy: position)] {
            trees += 1
        }
        position += right
        if position > (row.count - 1) {
        	position = position - row.count
        }
        count += down
    }
    return trees
}

